# Variable types can be cast to another types
# with use of
# int( )
# float( )
# str( )
# functions

print("Casting to int values: ")
x = int(1) # x will be 1
print(x)
x = int(2.8) # x will be 2
print(x)
x = int("3") # x will be 3
print(x)


print("Casting to float values: ")
y = float(4) # y will be 4.0
print(y)
y = float(2.8)  # y will be 2.8
print(y)
y = float("5")  # y will be 5.0
print(y)
y = float("4.2")  # y will be 4.2
print(y)

print("Casting to string values: ")
z = str(x) # z will be '3'
print(z)
z = str(y) # z will be '2.8'
print(z)
z = str("str") # z will be 'str'
print(z)
