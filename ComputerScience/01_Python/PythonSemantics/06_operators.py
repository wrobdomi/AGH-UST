# Python operators

# Arithmetic operators
x = 10
y = 5
z = 3

print("--- Arithmetic operators: +, -, *, /, %, //")
print("Addition x+y")
print(x+y)
print("Substract x-y")
print(x-y)
print("Multiply x*y")
print(x*y)
print("Divisionx/y")
print(x/y)
print("Division x/z")
print(x/z)
print("Modulo x%z")
print(x%z)
print("Floor division x//z")
print(x//z)

# Assignment operators
print("--- Assignment operators: +=, -=, *=, /=")
x = 10
print(x)
x += 5
print(x)
x -= 5
print(x)
x *= 5
print(x)
x /= 5
print(x)

# Comparison operators 
print("--- Comparison operators: ==, <, >, !=, >=, <=")
print(x==y)
print(x<y)
print(x>y)
print(x!=y)
print(x>=y)
print(x<=y)


# Logical operators
print("--- Logical operators: and, or, not")
print(x > y and x != y)
print(x < y or x > y)
print(not(x <= y))